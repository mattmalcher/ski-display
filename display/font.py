def _compile(src):
    out = {}
    for ch, rows in src.items():
        w = len(rows[0])
        cols = []
        for c in range(w):
            b = 0
            for r in range(7):
                if c < len(rows[r]) and rows[r][c] == '#':
                    b |= (1 << r)
            cols.append(b)
        out[ch] = cols
    return out


